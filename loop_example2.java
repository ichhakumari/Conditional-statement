import java.util.*;

public class loop_example2 {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter  number:");
        int n = sc.nextInt();
        
      for(int i=1; i<11; i++){
        System.out.println(i*n);
      }

    }
}

